interface PixelSpriteProps {
  persona: string;
  color: string;
  size?: number;
  isWalking?: boolean;
  isTalking?: boolean;
  direction?: 'down' | 'left' | 'right' | 'up';
}

// Color indices: 0=transparent, 1=outline, 2=skin, 3=hair, 4=primary(persona color), 5=secondary(darker), 6=white/highlight
type SpriteFrames = number[][][]; // [frame][row][col]

function darken(hex: string, amount: number): string {
  const r = Math.max(0, parseInt(hex.slice(1, 3), 16) - amount);
  const g = Math.max(0, parseInt(hex.slice(3, 5), 16) - amount);
  const b = Math.max(0, parseInt(hex.slice(5, 7), 16) - amount);
  return `#${r.toString(16).padStart(2, '0')}${g.toString(16).padStart(2, '0')}${b.toString(16).padStart(2, '0')}`;
}

function buildPalette(personaColor: string): string[] {
  return [
    'transparent',      // 0
    '#2a1f14',          // 1 outline
    '#f0c48a',          // 2 skin
    '#5c3a1e',          // 3 hair
    personaColor,       // 4 primary
    darken(personaColor, 60), // 5 secondary
    '#ffffff',          // 6 white
    '#e8d5b0',          // 7 light skin / parchment
    '#8b7355',          // 8 brown (boots/belt)
    '#c0c0c0',          // 9 metal/gray
  ];
}

