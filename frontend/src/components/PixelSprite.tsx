function getSprite(persona: string): SpriteFrames {
  return SPRITES[persona] || SPRITES[SPRITE_ALIASES[persona]] || SPRITES['knight'];
}

export default function PixelSprite({ persona, color, size = 64, isWalking, isTalking, direction = 'down' }: PixelSpriteProps) {
  const [frame, setFrame] = useState(0);
  const sprite = getSprite(persona);
  const palette = buildPalette(color);
  const pixelSize = size / 16;

  // Walk animation — alternate frames
  useEffect(() => {
    if (!isWalking && !isTalking) {
      setFrame(0);
      return;
    }
    const interval = setInterval(() => {
      setFrame(f => (f + 1) % 2);
    }, isTalking ? 400 : 250);
    return () => clearInterval(interval);
  }, [isWalking, isTalking]);

  const currentFrame = sprite[frame % sprite.length];

  // Flip for direction
  const flipX = direction === 'right';

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${size} ${size}`}
      style={{
        transform: flipX ? 'scaleX(-1)' : undefined,
        imageRendering: 'pixelated',
      }}
    >
      {currentFrame.map((row, y) =>
        row.map((colorIdx, x) => {
          if (colorIdx === 0) return null;
          const fill = palette[colorIdx] || palette[1];
          return (
            <rect
              key={`${x}-${y}`}
              x={x * pixelSize}
              y={y * pixelSize}
              width={pixelSize + 0.5}
              height={pixelSize + 0.5}
              fill={fill}
            />
          );
        })
      )}
    </svg>
  );
}
